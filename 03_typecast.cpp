// In this program we have printed the word ASCII code by typecasting char a into int a 

#include <iostream>
using namespace std;

int main() {

    int a = 'a'; // this will give the ASCII code of a.
    cout << a << endl;

    char ch = 97; // this will give the character of the particular ASCII code
    cout << ch << endl;

    char ch1 = 123456; // we are storing value more than max value of char
    cout << ch1 << endl;
}